    //Integer values validations
    private void checkInteger(String checkThis, String fieldValue) {
        try {
            if (Double.parseDouble(checkThis) % 1 != 0) {
                error = 1;
                JOptionPane.showMessageDialog(null, fieldValue + " should not be in fraction", "ERROR", 0);
            }
        } catch (Exception e) {
            error = 1;
            JOptionPane.showMessageDialog(null, fieldValue + " should be in Number format", "ERR0R", 0);
        }
    }
    //String values validations function
    private void checkString(String checkThis, String fieldValue) {
        try {
            if (Double.valueOf(Double.parseDouble(checkThis)) >= 0) {
                error = 1;
                JOptionPane.showMessageDialog(null, "Please enter " + fieldValue + " in proper format", "Error", 0);
            }
        } catch (Exception e) {

        }
    }
    //Date field values validations function
    private void checkDate(String checkThis, String fieldValue) {
         try
        {
            if(Double.valueOf(Double.parseDouble(checkThis))>=0);
            {
                error=1;
                JOptionPane.showMessageDialog(null,"Please enter "+fieldValue +"Date in proper format", "Error", 0);
            }
        }
        catch(Exception e)
        {
                if(!getDateRegex(checkThis))
                {
                    error=1;
                    JOptionPane.showMessageDialog(null,"Please Enter "+fieldValue +"date in format MM-DD-YYYY","ERROR",0);
                }    
                    
                    
        }
    }
    
    private boolean getDateRegex(String date)
    {
        //String regex= "\\d{2}\\-\\d{2}\\-\\d{4}";
        String dRegex="^(0[0-9]|1[0-2])\\-(0[1-9]|1[0-9]|2[0-9]|3[0-1])\\-(199[0-9]|20[0-1][0-8])";
        final Pattern pattern = Pattern.compile(dRegex);
        boolean bool=pattern.matcher(date).matches();
        return bool;
            
    }

  

