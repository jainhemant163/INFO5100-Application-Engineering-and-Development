    private void addCarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCarbtnActionPerformed
        // TODO add your handling code here:

        error = 0;
        String modelName = modelNametxt.getText();
        String modelNo = modelNotxt.getText();
        String manufac = manufacturertxt.getText();
        String manufacYr = manufacturerYeartxt.getText();
        String availCity = availableCitytxt.getText();
        String maintainCert = maintainenceCerttxt.getText();
        String serNo = serialNotxt.getText();
        String maxSeat = maximumSeattxt.getText();
        String minSeat = minimumSeattxt.getText();
        Boolean avail;

        if (BTNgrup.getSelection() == availableRadiobtn.getModel()) {
            avail = true;

        } else {
            avail = false;
        }

        //Validation code for each text field are added here:
        checkInteger(modelNo, "Model Number");
        checkString(modelName, "Model Name");
        checkString(manufac, "Manufacturer");
        checkDate(manufacYr,"Manufacturing Year");
        checkString(availCity, "Available City");
        checkDate(maintainCert,"Maintain Cert Expiry");
        checkInteger(serNo, "Serial Number");
        checkInteger(maxSeat, "Maximum Seat");
        checkInteger(minSeat, "Minimum Seat");

        //Continuation of the code
        if (Integer.parseInt(minSeat) > Integer.parseInt(maxSeat)) {
            error = 1;
            JOptionPane.showMessageDialog(null, "Maximum Seat Should be more than Minimum seat", "Error", 0);
        }

        if (error != 1) {
            //Code to add the new car details
            Car car = new Car();
            car.setModelName(modelName);
            car.setModelNo(modelNo);
            car.setManufacturer(manufac);
            car.setMaintainCert(maintainCert);
            car.setManufactureyear(manufacYr);
            car.setMaxSeat(Integer.parseInt(maxSeat));
            car.setMinSeat(Integer.parseInt(minSeat));
            car.setAvailability(avail);
            car.setAvailableCity(availCity);
            car.setSerialNo(serNo);

            carDirectory.addCarList(car);
            JOptionPane.showMessageDialog(null, "Car Details added Successfully");

            modelNametxt.setText("");
            modelNotxt.setText("");
            manufacturertxt.setText("");
            manufacturerYeartxt.setText("");
            maintainenceCerttxt.setText("");
            availableCitytxt.setText("");
            maximumSeattxt.setText("");
            minimumSeattxt.setText("");
            serialNotxt.setText("");
    }//GEN-LAST:event_addCarbtnActionPerformed
    }
